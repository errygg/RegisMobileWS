package edu.regis.ws.models;

public class School {
	
	protected Integer id;
	protected String name;
	
	public Integer getId() {
		return id;
	}
	
	public void setId( Integer value ) {
		this.id = value;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName( String value ) {
		this.name = value;
	}

}
